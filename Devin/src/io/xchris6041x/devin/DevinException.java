package io.xchris6041x.devin;

public class DevinException extends Exception {

	public DevinException() {
		super();
	}
	public DevinException(String message) {
		super(message);
	}
	public DevinException(Throwable throwable) {
		super(throwable);
	}
	public DevinException(String message, Throwable throwable) {
		super(message, throwable);
	}
	
}
